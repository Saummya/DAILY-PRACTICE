#User function Template for python3

class Solution:
    ##Complete this function
    #Function to find the sum of contiguous subarray with maximum sum.
   
    def maxSubArraySum(self,arr,N):
        ##Your code here
        max_so_far= arr[0]
        max1=0
        for i in range(N):
            max1+=arr[i]
            max_so_far=max(max_so_far,max1)
            if max1<0:
                max1=0
            
        return max_so_far
            

